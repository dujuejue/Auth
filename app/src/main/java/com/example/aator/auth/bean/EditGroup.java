public class EditGroup {
    private String name;
    private List<EditItem> editItems = new ArrayList<>();

    public EditGroup(String name, List<EditItem> editItems) {
        this.name = name;
        this.editItems = editItems;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<EditItem> getEditItems() {
        return editItems;
    }

    public void setEditItems(ArrayList<EditItem> editItems) {
        this.editItems = editItems;
    }
}
